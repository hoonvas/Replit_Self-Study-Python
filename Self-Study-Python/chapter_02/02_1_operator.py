# 문자열 연산자: +
print("안녕" + "하세요")
print("안녕하세요" + "!")
print("안녕하세요" + "1")

# 문자열 반복 연산자: *
print("안녕하세요" * 3)
print(3 * "안녕하세요")

# 문자열 범위 선택 연산자(슬라이싱): [:]
print("안녕하세요"[1:4])
print("안녕하세요"[0:2])
print("안녕하세요"[1:3])
print("안녕하세요"[2:4])
print("안녕하세요"[1:])
print("안녕하세요"[:3])

# 문자열의 길이 구하기
print(len("안녕하세요"))